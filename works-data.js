function renderWorksByCategory(containerId, data, subcategoryKey = null) {
    const container = document.getElementById(containerId);
    if (!container) return;

    container.innerHTML = '';

    if (subcategoryKey) {
        const category = data.categories[subcategoryKey];
        if (!category) return;
        category.works.forEach(work => {
            container.appendChild(createWorkCard(work));
        });
    } else {
        data.works.forEach(work => {
            container.appendChild(createWorkCard(work));
        });
    }
}

function createWorkCard(work) {
    const card = document.createElement('div');
    card.className = `work-card ${work.isPlaceholder ? 'work-card-placeholder' : ''}`;
    card.dataset.workId = work.id;

    if (work.isPlaceholder) {
        card.innerHTML = `
            <div class="work-card-placeholder-content">
                <i class="fa-solid fa-clock"></i>
                <span>即将上线</span>
            </div>
            <div class="work-card-info">
                <h4>${work.title}</h4>
                <p>${work.description}</p>
                <div class="work-tags">
                    ${work.tags.map(tag => `<span class="work-tag">${tag}</span>`).join('')}
                </div>
            </div>
        `;
    } else {
        card.innerHTML = `
            <div class="work-card-image">
                ${work.thumbnail ? `<img src="${work.thumbnail}" alt="${work.title}">` : '<div class="work-card-no-image">暂无图片</div>'}
                <div class="work-card-overlay">
                    <span class="view-detail-btn">查看详情</span>
                </div>
            </div>
            <div class="work-card-info">
                <h4>${work.title}</h4>
                <p>${work.description}</p>
                <div class="work-tags">
                    ${work.tags.map(tag => `<span class="work-tag">${tag}</span>`).join('')}
                </div>
            </div>
        `;

        card.addEventListener('click', () => {
            if (work.externalLink) {
                window.open(work.externalLink, '_blank');
            } else if (work.detailPage) {
                window.location.href = work.detailPage;
            }
        });
    }

    return card;
}

function initHorizontalScroll(containerId) {
    const container = document.getElementById(containerId);
    if (!container) return;

    let isDown = false;
    let startX;
    let scrollLeft;

    container.addEventListener('mousedown', (e) => {
        isDown = true;
        container.style.cursor = 'grabbing';
        startX = e.pageX - container.offsetLeft;
        scrollLeft = container.scrollLeft;
    });

    container.addEventListener('mouseleave', () => {
        isDown = false;
        container.style.cursor = 'grab';
    });

    container.addEventListener('mouseup', () => {
        isDown = false;
        container.style.cursor = 'grab';
    });

    container.addEventListener('mousemove', (e) => {
        if (!isDown) return;
        e.preventDefault();
        const x = e.pageX - container.offsetLeft;
        const walk = (x - startX) * 2;
        container.scrollLeft = scrollLeft - walk;
    });

    container.addEventListener('wheel', (e) => {
        if (window.innerWidth > 768) {
            e.preventDefault();
            container.scrollLeft += e.deltaY;
        }
    }, { passive: false });
}

